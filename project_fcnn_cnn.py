cnn_model = train_model(cnn_model, train_loader, epochs=EPOCHS, lr=LEARNING_RATE)

def metric_calculation(model, dataloader):
    device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    model.eval()

    all_predictions = []
    all_targets = []
    all_confidences = []

    with torch.no_grad():
        for images, labels in dataloader:
            images, labels = images.to(device), labels.to(device)
            outputs = model(images)

            # Apply Softmax to get probability distribution
            probs = torch.softmax(outputs, dim=1)
            # Find probability value of selected class and its label
            confidences, predicted = torch.max(probs, dim=1)

            all_predictions.extend(predicted.cpu().numpy())
            all_targets.extend(labels.cpu().numpy())
            all_confidences.extend(confidences.cpu().numpy())

    # Confusion matrix
    num_classes = 10
    confusion_matrix = np.zeros((num_classes, num_classes), dtype=int)
    for true_lbl, pred_lbl in zip(all_targets, all_predictions):
        confusion_matrix[true_lbl, pred_lbl] += 1

    accuracy = (np.array(all_predictions) == np.array(all_targets)).mean() * 100
    avg_confidence = np.mean(all_confidences) * 100

    return accuracy, avg_confidence, confusion_matrix

