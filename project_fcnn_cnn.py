class MNISTDataset(Dataset):
  def __init__(self, csv_file, transform=None, shift_place=0):
    self.df = pd.read_csv(csv_file)
    self.transform = transform
    self.shift_place = shift_place

  def __len__(self):
    return len(self.df)

  def __getitem__(self, idx):
        label = self.df.iloc[idx]['label']
        image = self.df.iloc[idx].drop('label').values.astype('float32')
        image = torch.tensor(image, dtype=torch.float32).view(28, 28) / 255.0 # To convert from NumPy to Tensor and apply Min-Max Scaling

        if self.shift_place > 0:
            image = self.shift_pixel(image, self.shift_place)

        image = image.unsqueeze(0) # To add the channel dimension.
        if self.transform:
            image = self.transform(image)
        return image, label  # This return the img shape as (784,).

  def shift_pixel(self, img_tensor, shift):
      shifted = torch.zeros_like(img_tensor)
      if shift < img_tensor.shape[1]:
          shifted[:, shift:] = img_tensor[:, :-shift]
      return shifted

