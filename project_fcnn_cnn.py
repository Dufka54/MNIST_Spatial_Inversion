  fnn.eval()
  cnn.eval()

  fig, axes = plt.subplots(num_img, 2, figsize=(10, num_img * 3.5))

  for i in range(num_img):
    img_nor, label = dataset_normal[i]
    img_shift, _ = dataset_shifted[i]

    normal_tensor = img_nor.unsqueeze(0).to(device)
    shift_tensor = img_shift.unsqueeze(0).to(device)

    with torch.no_grad():
      fnn_normal_pred = torch.argmax(fnn(normal_tensor)).item()
      fnn_shift_pred = torch.argmax(fnn(shift_tensor)).item()

      cnn_normal_pred = torch.argmax(cnn(normal_tensor)).item()
      cnn_shift_pred = torch.argmax(cnn(shift_tensor)).item()

    ax_normal = axes[i, 0]
    ax_normal.imshow(img_nor.squeeze().numpy(), cmap='gray')
    ax_normal.set_title(
        f'Original (True Label: {label}) \n'
        f'FNN Prediction: {fnn_normal_pred} | CNN Prediction: {cnn_normal_pred}',
    )

    ax_normal.axis('off')

    ax_shift = axes[i, 1]
    ax_shift.imshow(img_shift.squeeze().numpy(), cmap='plasma')
    ax_shift.set_title(
        f'Shifted Right by 5 (True Label: {label}) \n'
        f'FNN Prediction: {fnn_shift_pred} | CNN Prediction: {cnn_shift_pred}',
        fontsize = 10
    )

    ax_shift.axis('off')

