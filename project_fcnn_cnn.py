def plot_confusion(fnn_normal_cm, fnn_shift_cm, cnn_normal_cm, cnn_shift_cm):
    fig, axes = plt.subplots(2, 2, figsize=(14, 12))
    matrices = [
        (fnn_normal_cm, "FNN - Standard Test Set"),
        (fnn_shift_cm, "FNN - Shifted Test Set (+5 Right)"),
        (cnn_norm_cm, "CNN - Standard Test Set"),
        (cnn_shift_cm, "CNN - Shifted Test Set (+5 Right)")
    ]

    for idx, (matrix, title) in enumerate(matrices):
        ax = axes[idx // 2, idx % 2]
        im = ax.imshow(matrix, cmap='Blues', interpolation='nearest')

        fig.colorbar(im, ax=ax, fraction=0.046, pad=0.04)

        ax.set_title(title, fontsize=12, fontweight='bold', pad=10)
        ax.set_xticks(np.arange(10))
        ax.set_yticks(np.arange(10))
        ax.set_xticklabels(np.arange(10))
        ax.set_yticklabels(np.arange(10))
        ax.set_xlabel("Predicted Label")
        ax.set_ylabel("True Label")

        threshold = matrix.max() / 2.0
        for i in range(10):
            for j in range(10):
                color = "white" if matrix[i, j] > threshold else "black"
                ax.text(j, i, format(matrix[i, j], 'd'),
                        ha="center", va="center", color=color, fontsize=9)

    plt.suptitle("Confusion Matrix Analysis: Architectural Robustness Comparison", fontsize=16, fontweight='bold', y=0.98)
    plt.tight_layout()
    plt.show()

